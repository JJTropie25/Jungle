type I18nContextValue = {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: string, params?: Record<string, string | number>) => string;
};

const fallbackI18nContext: I18nContextValue = {
  language: "en",
  setLanguage: () => undefined,
  t: (key: string, params?: Record<string, string | number>) => {
    const dict = translations.en;
    let str = dict[key] ?? translations.en[key] ?? key;
    if (params) {
      for (const [k, v] of Object.entries(params)) {
        str = str.replace(new RegExp(`\{${k}\}`, "g"), String(v));
      }
    }
    return str;
  },
};

const I18nContext = createContext<I18nContextValue>(fallbackI18nContext);
const LANGUAGE_STORAGE_KEY = "jungle.language";

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguage] = useState<Language>("en");
  useEffect(() => {
    let mounted = true;
    AsyncStorage.getItem(LANGUAGE_STORAGE_KEY)
      .then((stored) => {
        if (!mounted || !stored) return;
        if (stored in translations) {
          setLanguage(stored as Language);
        }
      });
    return () => {
      mounted = false;
    };
  }, []);

  const value = useMemo<I18nContextValue>(() => {
    const t = (key: string, params?: Record<string, string | number>) => {
      const dict = translations[language] ?? translations.en;
      let str = dict[key] ?? translations.en[key] ?? key;
      if (params) {
        for (const [k, v] of Object.entries(params)) {
          str = str.replace(new RegExp(`\{${k}\}`, "g"), String(v));
        }
      }
      return str;
    };
    const setLanguageSafe = (lang: Language) => {
      setLanguage(lang);
      AsyncStorage.setItem(LANGUAGE_STORAGE_KEY, lang).catch(() => null);
    };
    return { language, setLanguage: setLanguageSafe, t };
  }, [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  return useContext(I18nContext);
}
